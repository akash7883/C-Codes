// C Codes For basic datatypes
#include<stdio.h>
int main()
{
	//int datatype
	int a;
	a=10;
	printf("Integer Datatype=%d",a);
	float b;
	b=10.5000000;
	printf("\nFloating Datatype=%f",b);
	double c;
	c=11.56789123;
	printf("\nDouble Datatype=%lf",c);
	char d;
	d='A';
	printf("\nChar =%c",d);
	
	
return 0;
}

